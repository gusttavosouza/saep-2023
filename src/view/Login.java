        String senha = new String(this.senha.getPassword());
        
        Main.professor = ProfessorDAO.BuscarProfessorPorEmail(email);
        if(Main.professor == null){
            JOptionPane.showMessageDialog(null, "USUÁRIO NÃO CADASTRADO NO BANCO");
            return;
        }
        
        if(!Main.professor.getSenha().equals(senha)){
           JOptionPane.showMessageDialog(null, "SENHA INCORRETA");
           return;
        }
        this.dispose();
        new HomeProfessor().setVisible(true);

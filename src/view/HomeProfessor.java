    void loadTurmas() {
        this.turmas = TurmaDAO.buscarTurmasPorProfessor(Main.professor.getId());
        this.panelPrincipal.removeAll();
        for (Turma turma : turmas) {
            this.panelPrincipal.add(new LinhaTabelaTurma(turma, this));
        }
    }
    
